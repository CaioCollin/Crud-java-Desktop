    private void CadastrarJogo() {
        String nome_jogo;
        nome_jogo = txtNomeJogo.getText();

        jogosDTO objjogosDTO = new jogosDTO();
        objjogosDTO.setNome_jogo(nome_jogo);

        jogosDAO objjogosDAO = new jogosDAO();
        objjogosDAO.cadastrarFuncionario(objjogosDTO);
    }

    private void ListarValoreJogos() {

        try {
            jogosDAO objjogoDAO = new jogosDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaJogo.getModel();
            model.setNumRows(0);

            ArrayList<jogosDTO> lista = objjogoDAO.pesquisarJogos();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId_jogo(),
                    lista.get(num).getNome_jogo()

                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ListarValoresJogos VIEW" + e);
        }

    }

    private void CarregarCampos() {

        int setar = tabelaJogo.getSelectedRow();

        txtcodigoJogo.setText(tabelaJogo.getModel().getValueAt(setar, 0).toString());
        txtNomeJogo.setText(tabelaJogo.getModel().getValueAt(setar, 1).toString());

    }

    private void limparCampos() {
        txtcodigoJogo.setText("");
        txtNomeJogo.setText("");
        txtNomeJogo.requestFocus();
    }
    
    private void AletarCampos(){
        int id_jogos;
        String nome_jogos;

        id_jogos = Integer.parseInt(txtcodigoJogo.getText());
        nome_jogos = txtNomeJogo.getText();

        jogosDTO objjogosDTO = new jogosDTO();
        objjogosDTO.setId_jogo(id_jogos);
        objjogosDTO.setNome_jogo(nome_jogos);
        
        jogosDAO objjogosDAO = new jogosDAO();
        objjogosDAO.AlterarJogos(objjogosDTO);
    }
    
    
    
    private void ExcluirJogos(){
        int id_jogos;
        
        id_jogos = Integer.parseInt(txtcodigoJogo.getText());
        
        jogosDTO objjogosDTO = new jogosDTO();
        objjogosDTO.setId_jogo(id_jogos);
        
        jogosDAO objjogosDAO = new jogosDAO();
        objjogosDAO.ExcluirFuncionario(objjogosDTO);

    }




















